<p>
  <strong>"EL LOCATARIO"</strong> deberá devolver el inmueble arrendado al
  vencimiento del plazo estipulado o rescisión del presente,
  sin excusas, demoras, ni novación de ninguna naturaleza.
  No obstante, si por cualquier motivo el inmueble no fuera
  devuelto a su vencimiento, sin perjuicio del ejercicio de
  las acciones pertinentes por parte de <strong>"EL LOCADOR"</strong> para
  obtener el desalojo, <strong>"EL LOCATARIO"</strong> deberá abonar, en
  concepto de indemnización por ocupación ilegítima, una
  suma mensual igual al alquiler pactado, prorrateado en
  modo diario y mientras la falta de devolución subsista,
  sin perjuicio de otras sumas, intereses compensatorios o
  penalidades que pudieren corresponder a <strong>"EL LOCADOR"</strong> por
  el incumplimiento, y hasta que <strong>"EL LOCADOR"</strong> obtenga
  efectivamente la restitución del bien inmueble a su entera
  conformidad. Se pacta que dicha indemnización podrá ser
  reclamada por la misma vía ejecutiva que todas las
  prestaciones dinerarias a cargo de <strong>"EL LOCATARIO"</strong>. En caso
  de que <strong>"EL LOCADOR"</strong> estimare que los daños y perjuicios
  que le ocasionare la falta de entrega del inmueble en
  término fueran superiores a la indemnización pactada,
  podrá optar por reclamar dichos daños e intereses. Queda
  aclarado que la permanencia de <strong>"EL LOCATARIO"</strong> en el
  inmueble locado, después de vencido el término del
  contrato, en cualquier caso, incluso cuando fuera por
  voluntad del <strong>"EL LOCADOR"</strong> estará facultado para solicitar
  del Juez o Autoridad competente la inmediata restitución
  del inmueble.
</p>

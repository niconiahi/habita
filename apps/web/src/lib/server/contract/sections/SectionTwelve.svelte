<p>
  Será por cuenta de <strong>"EL LOCATARIO"</strong> la
  conservación de los artefactos y accesorios del inmueble y
  la reparación de los desperfectos menores provocados por
  el uso. <strong>"EL LOCATARIO"</strong> dará inmediata
  cuenta a <strong>"EL LOCADOR"</strong>, de cualquier
  desperfecto que sufriere la propiedad, y permitirá la
  ejecución de todo trabajo que sea necesario para su
  conservación o mejora, sin derecho a cobrar indemnización
  alguna por ello. <strong>"EL LOCATARIO"</strong> se obliga
  a permitir que <strong>"EL LOCADOR"</strong> o quien lo
  represente, pueda inspeccionar el inmueble locado en
  cualquier momento, dando aviso previo a
  <strong>"EL LOCATARIO"</strong>
  con 48 horas de anticipación. Serán a exclusivo cargo de
  <strong>"EL LOCADOR"</strong> las reparaciones, reconstrucciones
  y/o refracciones que fueran menester realizar en el inmueble
  por daños causados por fuerza mayor, vicios redhibitorios, o
  por el normal uso de la cosa, y sin derecho a reembolso alguno
  a su favor.
</p>

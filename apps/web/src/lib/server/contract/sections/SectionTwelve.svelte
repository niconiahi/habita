<p>
  Será por cuenta de "EL LOCATARIO" la conservación de los
  artefactos y accesorios del inmueble y la reparación de
  los desperfectos menores provocados por el uso. "EL
  LOCATARIO" dará inmediata cuenta a "EL LOCADOR", de
  cualquier desperfecto que sufriere la propiedad, y
  permitirá la ejecución de todo trabajo que sea necesario
  para su conservación o mejora, sin derecho a cobrar
  indemnización alguna por ello. "EL LOCATARIO" se obliga a
  permitir que "EL LOCADOR" o quien lo represente, pueda
  inspeccionar el inmueble locado en cualquier momento,
  dando aviso previo a "EL LOCATARIO" con 48 horas de
  anticipación. Serán a exclusivo cargo de "EL LOCADOR" las
  reparaciones, reconstrucciones y/o refracciones que fueran
  menester realizar en el inmueble por daños causados por
  fuerza mayor, vicios redhibitorios, o por el normal uso de
  la cosa, y sin derecho a reembolso alguno a su favor.
</p>
